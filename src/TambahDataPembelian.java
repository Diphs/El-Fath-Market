    private void btn_simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_simpanActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();

        String noTransaksi = txNoTransaksi.getText();
        String tanggal = txTanggal.getText();
        String KodeKaryawan = txt_kdKaryawan.getText();
        
        try {
            String sql = "INSERT INTO `pembelian`(`kd_pembelian`, `waktu`, `kd_karyawan`) VALUES ('"+noTransaksi+"','"
            +tanggal+"','"+KodeKaryawan+"')";
            java.sql.Connection conn=(Connection)Config.configDB();
            java.sql.PreparedStatement pst=conn.prepareStatement(sql);
            
            pst.executeUpdate();
            pst.close();
        } catch (Exception e) {
             JOptionPane.showMessageDialog(this, e.getMessage());
        }

        try {
            Connection c = (Connection)Config.configDB();
            int baris = jTable1.getRowCount();

            for (int i = 0; i < baris; i++) {
                String sql = "INSERT INTO `detail_pembelian`(`id_pbl`, `jumlah`, `total_harga`, `kd_produk`, `kd_pembelian`) VALUES (NULL, '"
                + jTable1.getValueAt(i, 3) +"','"+ jTable1.getValueAt(i, 5)
                +"','"+ jTable1.getValueAt(i, 1) +"','"+ jTable1.getValueAt(i, 0)
                +"')";
                PreparedStatement p = c.prepareStatement(sql);
                p.executeUpdate();
                p.close();
            }
        } catch (Exception e) {
            System.out.println("simpan Pembelianrinci error");
        }
        clear();
        utama();
        autonumber();
        kosong();
        txTampil.setText("Rp. 0");
        this.setVisible(false);
        new Pembelian().setVisible(true);
    }//GEN-LAST:event_btn_simpanActionPerformed

