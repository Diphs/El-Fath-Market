    private void btn_simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_simpanActionPerformed
        // TODO add your handling code here:
        try {
            String sql = "UPDATE karyawan SET nama='"+txt_nama.getText()
            +"',jenis_kelamin='"+txt_jk.getText()
            +"',alamat='"+txt_alamat.getText()
            +"',no_tlp='"+txt_noTlp.getText()
            +"' WHERE kd_karyawan ='"+txt_kdKaryawan.getText()+"'";
            java.sql.Connection conn = (Connection)Config.configDB();
            java.sql.PreparedStatement pst = conn.prepareStatement(sql);
            
            pst.execute();
            JOptionPane.showMessageDialog(null,"Selamat, Data Berhasil Di Ubah");
            this.setVisible(false);
            new Karyawan().setVisible(true);
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,e.getMessage());
        }
    }//GEN-LAST:event_btn_simpanActionPerformed

